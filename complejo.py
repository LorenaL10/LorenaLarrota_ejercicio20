import numpy as np

class Complejo():
    def __init__(self, a, b):
        self.imaginario=b
        self.real=a
        self.norma=np.sqrt((a**2)+(b**2))
        
    def conjugado(a,b):
        a, -self.imaginario
        
    def calcula_norma(a,b):
        self.norma
    
